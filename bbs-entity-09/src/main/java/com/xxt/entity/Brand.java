package com.xxt.entity;

import com.xxt.common.base.BaseEntity;

public class Brand extends BaseEntity {

    private static final long serialVersionUID = 1L;

    /** ID */
    private Integer id;
    /** 名称 */
    private String name;
    /** 描述 */
    private String description;
    /** 图片Url */
    private String imgUrl;
    /** 品牌网址 */
    private String webSite;
    /** 排序:最大最排前 */
    private Integer sort;
    /** 是否可见 1:可见 0:不可见 */
    private Short isDisplay;

    /*field end*/

    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getDescription() {
        return description;
    }
    public void setDescription(String description) {
        this.description = description;
    }
    public String getImgUrl() {
        return imgUrl;
    }
    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }
    public String getWebSite() {
        return webSite;
    }
    public void setWebSite(String webSite) {
        this.webSite = webSite;
    }
    public Integer getSort() {
        return sort;
    }
    public void setSort(Integer sort) {
        this.sort = sort;
    }
    public Short getIsDisplay() {
        return isDisplay;
    }
    public void setIsDisplay(Short isDisplay) {
        this.isDisplay = isDisplay;
    }

    /*method end*/

	@Override
	public String toString() {
		return "Brand [id=" + id + ", name=" + name + ", description=" + description + ", imgUrl=" + imgUrl + ", webSite=" + webSite + ", sort=" + sort + ", isDisplay=" + isDisplay+"]";
	}
}
