package com.xxt.entity;

import com.xxt.common.base.BaseEntity;

public class Employee extends BaseEntity {

    private static final long serialVersionUID = 1L;

    /** 用户名 */
    private String username;
    /** 密码 */
    private String password;
    /** 学历 */
    private String degree;
    /** 邮箱 */
    private String email;
    /** 性别  */
    private Short gender;
    /** 图片(头像) */
    private String imgUrl;
    /** 手机 */
    private String phone;
    /** 真实名字 */
    private String realName;
    /** 毕业学校 */
    private String school;
    /** 是否删除 1:未删除 0:删除 */
    private Short isDel;

    /*field end*/

    public String getUsername() {
        return username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getPassword() {
        return password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public String getDegree() {
        return degree;
    }
    public void setDegree(String degree) {
        this.degree = degree;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public Short getGender() {
        return gender;
    }
    public void setGender(Short gender) {
        this.gender = gender;
    }
    public String getImgUrl() {
        return imgUrl;
    }
    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }
    public String getPhone() {
        return phone;
    }
    public void setPhone(String phone) {
        this.phone = phone;
    }
    public String getRealName() {
        return realName;
    }
    public void setRealName(String realName) {
        this.realName = realName;
    }
    public String getSchool() {
        return school;
    }
    public void setSchool(String school) {
        this.school = school;
    }
    public Short getIsDel() {
        return isDel;
    }
    public void setIsDel(Short isDel) {
        this.isDel = isDel;
    }

    /*method end*/

	@Override
	public String toString() {
		return "Employee [username=" + username + ", password=" + password + ", degree=" + degree + ", email=" + email + ", gender=" + gender + ", imgUrl=" + imgUrl + ", phone=" + phone + ", realName=" + realName + ", school=" + school + ", isDel=" + isDel+"]";
	}
}
